/**
 * @author Puvana Swatvanith 
 * The super class of all of Task to call the task to
 * run the stopwatch and calculated the elapsed time.
 */
public class Task {

    /**
     * The function to call the subclass-run method.
     */
    public void run() {
        // Call the run-method of subclass.
        this.run();
    }
}